#Книжные магазины предлагают следующие коллекции книг.
#Магистр – Лермонтов, Достоевский, Пушкин, Тютчев
#ДомКниги – Толстой, Грибоедов, Чехов, Пушкин.
#БукМаркет – Пушкин, Достоевский, Маяковский.
#Галерея – Чехов, Тютчев, Пушкин.
#Определить в каких магазинах
#нельзя приобрести книги Грибоедова и Маяковского

# Определяем коллекции книг в каждом магазине
stores = {
    "Магистр": ["Лермонтов", "Достоевский", "Пушкин", "Тютчев"],
    "ДомКниги": ["Толстой", "Грибоедов", "Чехов", "Пушкин"],
    "БукМаркет": ["Пушкин", "Достоевский", "Маяковский"],
    "Галерея": ["Чехов", "Тютчев", "Пушкин"]
}

# Книги, которые нужно проверить
books_to_check = ["Грибоедов", "Маяковский"]

# Определяем магазины, где нельзя приобрести указанные книги
stores_without_books = []

for store, books in stores.items():
    if all(book not in books for book in books_to_check):
        stores_without_books.append(store)

print("Магазины, где нельзя приобрести книги Грибоедова и Маяковского:", stores_without_books)
