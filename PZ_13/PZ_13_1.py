#В двумерном списке элементы первого столбца возвести в куб.

# Исходный двумерный список
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
# Возведение элементов первого столбца в куб
for row in matrix:
    row[0] = row[0] ** 3
# Вывод результата
print("Обновленный двумерный список:")
for row in matrix:
    print(row)