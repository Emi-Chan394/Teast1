# Вводим значения A и B
A = int(input("Введите значение A: "))
B = int(input("Введите значение B: "))

# Проверяем условие A < B
if A < B:
    # Находим сумму всех целых чисел от A до B включительно
    total_sum = sum(range(A, B + 1))
    print(f"Сумма всех целых чисел от {A} до {B} включительно равна: {total_sum}")
else:
    print("Убедитесь, что A меньше B.")